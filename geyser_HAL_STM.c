void SendToESP32(float temp, int water_present) {
  char buffer[64];
  sprintf(buffer, "%.2f,%d\n", temp, water_present);
  HAL_UART_Transmit(&huart2, (uint8_t *)buffer, strlen(buffer), HAL_MAX_DELAY);
}
